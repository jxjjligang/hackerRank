let nimObjects = [];

function half(n) {
    if (n === 1)
        return 1;

    let nExponent = 1 + Math.floor(Math.log2(n));
    for (let i = (nimObjects.length === 0 ? 1 : nimObjects[nimObjects.length - 1].exponent + 1); i <= nExponent; i++) {
        let nimObject = { exponent: i, start: Math.pow(2, i - 1), end: Math.pow(2, i) - 1 };
        nimObjects.push(nimObject);
    }

    // countOfSameNim the count of number that has the same Nim value as the last Nim object 
    let lastNimObj = nimObjects[nimObjects.length - 1], countOfSameNim = n - lastNimObj.start + 1;
    let lastNimCountIsOdd = countOfSameNim % 2 === 1;   // lastNimCountIsOdd can be used to quickly calculate the XOR result for Nim value of numbers (from 1 to n)       
    let xorResult = lastNimCountIsOdd ? (1 ^ lastNimObj.exponent) : 1;
    if (xorResult === 0)
        return 0;
    else {
        for (let i = 0; i < nimObjects.length; i++) {
            let nimValue = nimObjects[i].exponent;
            let xor = xorResult ^ nimValue;
            if (xor < nimValue) {
                if (xor === 0)
                    return nimObjects[i].start;  // return numbers[i]

                // nimObjects[i] is the nim object that should be subtracted 
                let numberI = nimObjects[i].start, halfn = ((numberI % 2 === 0) ? numberI / 2 : (numberI - 1) / 2);

                // nimTarget is the nim object that should be subtracted to
                let nimTarget = nimObjects[xor - 1];
                if (nimTarget.end <= halfn)
                    return numberI - nimTarget.end;
                else
                    return numberI - halfn;
            }
        }
    }
}

