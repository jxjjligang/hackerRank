/*
 * Complete the towerBreakers function below.
 */
function towerBreakers(arr) {
    function getDivisors(number) {
        let divisors = [], found;
        do {
            found = false;
            for (let i = 2; i <= Math.ceil(Math.sqrt(number)); i++) {
                if (number % i === 0) {
                    found = true;
                    divisors.push(i);
                    number /= i;
                    break;
                }
            }
        }
        while (found === true)
        if (number > 1)
            divisors.push(number);
        return divisors;
    }

    let arrCopy = arr.slice();
    arrCopy = arrCopy.filter(element => element !== 1);
    if (arrCopy.length === 0)
        return 2;

    let divisorsArray = [];
    for (let num of arrCopy)
        divisorsArray.push(getDivisors(num));

    let xorBases = [];
    for (let divisors of divisorsArray) {
        if (divisors.length < 2)
            xorBases.push(1);
        else
            xorBases.push(divisors.length);
    }

    let xorResult = xorBases.reduce((agg, cur) => agg ^ cur);
    return xorResult !== 0 ? 1 : 2;
}

