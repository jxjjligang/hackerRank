function arraySplitting(arr) {
    function visit(node) {
        if (node === undefined)
            return 0;

        let leftLevel = visit(node.left), rightLevel = visit(node.right);
        return 1 + Math.max(leftLevel, rightLevel);
    }

    function createNode(start, end) {
        let middle = (start + end) / 2;
        if (valueSet.has(middle)) {
            let node = { value: middle };
            node.left = createNode(start, node.value);
            node.right = createNode(node.value, end);
            return node;
        }
        else
            return undefined;
    }

    if (arr.length <= 1)
        return 0;

    let sumArr = [arr[0]];
    for (let i = 1; i < arr.length; i++)
        sumArr[i] = sumArr[i - 1] + arr[i];;

    let valueSet = new Set(sumArr);
    if (valueSet.size === 1)
        return arr.length - 1;

    let root = createNode(0, sumArr[sumArr.length - 1]);
    if (root === undefined)
        return 0;
    else
        return visit(root);
}

