function indianJob(g, arr) {
    const YES = 'YES', NO = 'NO', ARR_LEN = arr.length, LIMIT = g;

    // dpArr is empty 
    function divide(half1, half2, values) {
        let sum1 = half1.reduce((a, b) => a + b, 0), sum2 = half2.reduce((a, b) => a + b, 0);
        if (sum1 > LIMIT && sum2 > LIMIT)
            return false;

        if (values.length === 0)
            return (sum1 <= LIMIT && sum2 <= LIMIT) ? true : false;

        let len = values.length, last = values[len - 1];
        if (sum1 + last > LIMIT) {
            half2.push(last);
            return divide(half1, half2, values.slice(0, len - 1));
        }
        else {
            // #1 
            half1.push(last);
            let result = divide(half1, half2, values.slice(0, len - 1));
            if (result === true)
                return result;
            else {
                half2.push(last);
                return divide(half1, half2, values.slice(0, len - 1));
            }
        }

    }

    if (arr.length === 1)
        return arr[0] <= g ? YES : NO;

    arr.sort((a, b) => a - b);
    let sum = arr.reduce((a, b) => a + b);
    if (sum > 2 * LIMIT)
        return NO;

    let half1 = [arr[ARR_LEN - 1]], half2 = [];
    return divide(half1, half2, arr.slice(0, ARR_LEN - 1)) ? YES : NO;
}

